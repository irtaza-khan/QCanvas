"""
Script: analyze_qasm.py
Paper 5 – Cross-Framework Quantum Algorithm Benchmarking
Phase 2 – Static QASM Analysis

This script reads all generated .qasm files from benchmarks/qasm_outputs/
and extracts structural metrics WITHOUT running any simulation.

Metrics extracted per QASM file:
  - total_gates          : Total gate invocation count
  - circuit_depth        : Longest path from first gate to measurement
  - gate_types           : Dict of gate_name → count (H, CX, RZ, etc.)
  - multi_qubit_gates    : Count of 2+ qubit gates (CX, CZ, CCX, SWAP, etc.)
  - multi_qubit_ratio    : multi_qubit_gates / total_gates
  - measurement_count    : Number of 'measure' operations
  - ctrl_modifier_count  : Count of 'ctrl @' QASM 3.0 modifier occurrences
  - inv_modifier_count   : Count of 'inv @' QASM 3.0 modifier occurrences
  - pow_modifier_count   : Count of 'pow(' QASM 3.0 modifier occurrences
  - qubit_count          : Number of qubits declared in the QASM header
  - num_parameters       : Number of symbolic parameters (input float declarations)

Output:
  - benchmarks/metrics/structural_metrics.csv

Called by:
  - benchmarks/notebooks/nb01_compile_and_static_analysis.ipynb

Dependencies:
  - All .qasm files generated by compile_all.py must exist in qasm_outputs/
"""

# ──────────────────────────────────────────────────────────
# Imports
# ──────────────────────────────────────────────────────────

